bool
Elf32Object::Taste(const llvm::MemoryBuffer& in,
                   /*@out@*/ std::string* arch_keyword,
                   /*@out@*/ std::string* machine)
{
    ElfConfig config;
    if (!config.ReadProgramHeader(in))
        return false;
    if (config.cls != ELFCLASS32)
        return false;
    if (config.machine_type == EM_386)
    {
        arch_keyword->assign("x86");
        machine->assign("x86");
        return true;
    }
    return false;
}

